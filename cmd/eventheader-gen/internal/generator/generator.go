type Diagnostic struct {
	Position token.Position
	Message  string
}

func (d *Diagnostic) Error() string {
	if d.Position.IsValid() {
		return fmt.Sprintf("%s: eventheader-gen: %s", d.Position, d.Message)
	}
	return "eventheader-gen: " + d.Message
}

