func render(packageName, buildConstraint string, events []event) ([]byte, error) {
	var out strings.Builder
	if buildConstraint != "" {
		fmt.Fprintf(&out, "//go:build %s\n\n", buildConstraint)
	}
	out.WriteString(GeneratedMarker + "\n")
	out.WriteString(generatedSyntax + "\n\n")
	fmt.Fprintf(&out, "package %s\n\n", packageName)
	out.WriteString("import (\n")
	out.WriteString("\teventheader_gen_fmt \"fmt\"\n")
	out.WriteString("\teventheader_gen_sync \"sync\"\n")
	out.WriteString("\n\teventheader_gen_eventheader \"github.com/cataggar/LinuxTracepoints-Go/eventheader\"\n")
	out.WriteString("\teventheader_gen_userevents \"github.com/cataggar/LinuxTracepoints-Go/userevents\"\n")
	out.WriteString(")\n\n")
	for _, ev := range events {
		renderEvent(&out, ev)
	}
	formatted, err := format.Source([]byte(out.String()))
	if err != nil {
		return nil, fmt.Errorf("format generated source: %w\n%s", err, out.String())
	}
	return formatted, nil
}

func renderEvent(out *strings.Builder, ev event) {
	prefix := "eventheaderGen" + ev.TypeName
	fmt.Fprintf(out, "var (\n\t%sSchemaOnce eventheader_gen_sync.Once\n", prefix)
	fmt.Fprintf(out, "\t%sSchemaValue *eventheader_gen_eventheader.Schema\n", prefix)
	fmt.Fprintf(out, "\t%sSchemaErr error\n)\n\n", prefix)
	fmt.Fprintf(out, "// %sSchema returns the immutable schema for %s.\n", ev.TypeName, ev.TypeName)
	fmt.Fprintf(out, "func %sSchema() (*eventheader_gen_eventheader.Schema, error) {\n", ev.TypeName)
	fmt.Fprintf(out, "\t%sSchemaOnce.Do(func() {\n", prefix)
	fmt.Fprintf(out, "\t\t%sSchemaValue, %sSchemaErr = eventheader_gen_eventheader.NewSchema(\n", prefix, prefix)
	fmt.Fprintf(out, "\t\t\teventheader_gen_eventheader.SchemaOptions{Name: %q, ID: %d, Version: %d, Tag: %d, Opcode: %d},\n",
		ev.Options.Name, ev.Options.ID, ev.Options.Version, ev.Options.Tag, ev.Options.Opcode)
	for _, f := range ev.Fields {
		fmt.Fprintf(out, "\t\t\t%s,\n", schemaExpression(f))
	}
	out.WriteString("\t\t)\n\t})\n")
	fmt.Fprintf(out, "\treturn %sSchemaValue, %sSchemaErr\n}\n\n", prefix, prefix)
	fmt.Fprintf(out, "// %sWriter is goroutine-local reusable state for %s.\n", ev.TypeName, ev.TypeName)
	fmt.Fprintf(out, "type %sWriter struct {\n\tevent *eventheader_gen_eventheader.Event\n\tbinding eventheader_gen_eventheader.Binding\n", ev.TypeName)
	renderScratchFields(out, ev.Fields)
	out.WriteString("}\n\n")
	fmt.Fprintf(out, "// New%sWriter binds %s to provider. A writer must be used by only one goroutine.\n", ev.TypeName, ev.TypeName)
	fmt.Fprintf(out, "func New%sWriter(provider *eventheader_gen_eventheader.Provider, storage []byte) (*%sWriter, error) {\n", ev.TypeName, ev.TypeName)
	fmt.Fprintf(out, "\tschema, err := %sSchema()\n\tif err != nil { return nil, err }\n", ev.TypeName)
	fmt.Fprintf(out, "\tset, err := provider.EventSet(%d, %#x, %q)\n\tif err != nil { return nil, err }\n", ev.Options.Level, ev.Options.Keyword, ev.Options.Group)
	out.WriteString("\tevent, err := eventheader_gen_eventheader.NewEvent(set, schema)\n\tif err != nil { return nil, err }\n")
	fmt.Fprintf(out, "\treturn &%sWriter{event: event, binding: event.Bind(storage)}, nil\n}\n\n", ev.TypeName)
	fmt.Fprintf(out, "// Enabled reports whether the %s event is enabled.\n", ev.Options.Name)
	fmt.Fprintf(out, "func (w *%sWriter) Enabled() bool { return w != nil && w.event != nil && w.event.Enabled() }\n\n", ev.TypeName)
	fmt.Fprintf(out, "// Event returns the reusable runtime event.\nfunc (w *%sWriter) Event() *eventheader_gen_eventheader.Event {\n", ev.TypeName)
	out.WriteString("\tif w == nil { return nil }\n\treturn w.event\n}\n\n")
	fmt.Fprintf(out, "// Write encodes value and emits it with optional activity correlation IDs.\nfunc (w *%sWriter) Write(value *%s, activity, related *eventheader_gen_eventheader.ActivityID) error {\n", ev.TypeName, ev.TypeName)
	out.WriteString("\tif w == nil || w.event == nil { return eventheader_gen_userevents.ErrClosed }\n")
	out.WriteString("\tif !w.event.Enabled() { return w.event.Write(nil, nil, nil) }\n")
	out.WriteString("\tif err := w.bind(value); err != nil { return err }\n")
	out.WriteString("\treturn w.event.Write(&w.binding, activity, related)\n}\n\n")
	fmt.Fprintf(out, "func (w *%sWriter) bind(value *%s) error {\n", ev.TypeName, ev.TypeName)
	out.WriteString("\tif value == nil { return eventheader_gen_fmt.Errorf(\"event value: %w\", eventheader_gen_eventheader.ErrInvalidValue) }\n")
	temp := 0
	renderScratchPreflight(out, ev.Fields, "value", "\t", &temp)
	out.WriteString("\tw.binding.Reset()\n")
	for _, f := range ev.Fields {
		renderBinding(out, f, "value."+f.GoName, "\t", &temp)
	}
	out.WriteString("\treturn nil\n}\n\n")
}

func renderScratchPreflight(out *strings.Builder, fields []field, expression, indent string, temp *int) {
	for _, f := range fields {
		fieldExpression := expression + "." + f.GoName
		if f.Constructor == "StructField" {
			renderScratchPreflight(out, f.Children, fieldExpression, indent, temp)
			continue
		}
		if f.Scratch != "" {
			renderScratchValidation(out, f, fieldExpression, indent, temp)
		}
	}
}

func renderScratchFields(out *strings.Builder, fields []field) {
	for _, f := range fields {
		if f.Constructor == "StructField" {
			renderScratchFields(out, f.Children)
			continue
		}
		if f.Scratch == "" {
			continue
		}
		scratchType := f.BaseType
		if f.Array != "" {
			scratchType = "[]" + scratchType
		}
		fmt.Fprintf(out, "\t%s %s\n", f.Scratch, scratchType)
		if f.ScratchBacking != "" {
			fmt.Fprintf(out, "\t%s %s\n", f.ScratchBacking, f.BaseType)
		}
	}
}

func fieldOptions(f field) string {
	if f.Format == "" && f.Tag == 0 {
		return ""
	}
	formatName := f.Format
	if formatName == "" {
		formatName = "FormatDefault"
	}
	return fmt.Sprintf(", eventheader_gen_eventheader.FieldOptions{Format: eventheader_gen_eventheader.%s, Tag: %d}", formatName, f.Tag)
}

func schemaExpression(f field) string {
	if f.Constructor == "StructField" {
		children := make([]string, len(f.Children))
		for i := range f.Children {
			children[i] = schemaExpression(f.Children[i])
		}
		return fmt.Sprintf("eventheader_gen_eventheader.StructField(%q, []eventheader_gen_eventheader.FieldDefinition{%s}%s)",
			f.WireName, strings.Join(children, ", "), fieldOptions(f))
	}
	expr := fmt.Sprintf("eventheader_gen_eventheader.%s(%q%s)", f.Constructor, f.WireName, fieldOptions(f))
	switch f.Array {
	case "fixed":
		expr = fmt.Sprintf("eventheader_gen_eventheader.ArrayField(%s, eventheader_gen_eventheader.ArrayFixed, %d)", expr, f.Count)
	case "variable":
		expr = fmt.Sprintf("eventheader_gen_eventheader.ArrayField(%s, eventheader_gen_eventheader.ArrayVariable)", expr)
	}
	return expr
}

func renderBinding(out *strings.Builder, f field, expression, indent string, temp *int) {
	if f.Constructor == "StructField" {
		for _, child := range f.Children {
			renderBinding(out, child, expression+"."+child.GoName, indent, temp)
		}
		return
	}
	valueExpr := expression
	if f.NetType != "" {
		*temp++
		name := fmt.Sprintf("eventheaderGenIP%d", *temp)
		if f.NetType == "netip" {
			test := "Is4()"
			convert := "As4()"
			if f.Semantic == "ipv6" {
				test, convert = "Is6()", "As16()"
			}
			fmt.Fprintf(out, "%sif !%s.%s { return eventheader_gen_fmt.Errorf(%q, eventheader_gen_eventheader.ErrInvalidValue) }\n", indent, expression, test, f.Path+": %w")
			fmt.Fprintf(out, "%s%s := %s.%s\n", indent, name, expression, convert)
		} else {
			convert := "To4()"
			if f.Semantic == "ipv6" {
				convert = "To16()"
				fmt.Fprintf(out, "%sif %s.To4() != nil { return eventheader_gen_fmt.Errorf(%q, eventheader_gen_eventheader.ErrInvalidValue) }\n", indent, expression, f.Path+": %w")
			}
			fmt.Fprintf(out, "%s%sSlice := %s.%s\n", indent, name, expression, convert)
			fmt.Fprintf(out, "%sif %sSlice == nil { return eventheader_gen_fmt.Errorf(%q, eventheader_gen_eventheader.ErrInvalidValue) }\n", indent, name, f.Path+": %w")
			fmt.Fprintf(out, "%svar %s %s\n%scopy(%s[:], %sSlice)\n", indent, name, f.BaseType, indent, name, name)
		}
		valueExpr = name
	} else if f.Array != "" {
		if f.DirectArray {
			valueExpr = expression
			if f.FixedOuter {
				valueExpr += "[:]"
			}
		} else {
			name := "w." + f.Scratch
			renderScratchResize(out, name, "[]"+f.BaseType, expression, indent)
			if f.ScratchBacking != "" {
				backing := "w." + f.ScratchBacking
				count := f.Scratch + "Count"
				offset := f.Scratch + "Offset"
				fmt.Fprintf(out, "%s%s := 0\n", indent, count)
				fmt.Fprintf(out, "%sfor i := range %s { %s += len(%s[i]) }\n", indent, expression, count, expression)
				fmt.Fprintf(out, "%sif cap(%s) < %s { %s = make(%s, %s) } else { %s = %s[:%s] }\n",
					indent, backing, count, backing, f.BaseType, count, backing, backing, count)
				fmt.Fprintf(out, "%s%s := 0\n", indent, offset)
				elementType := strings.TrimPrefix(f.BaseType, "[]")
				fmt.Fprintf(out, "%sfor i := range %s {\n", indent, expression)
				fmt.Fprintf(out, "%s\tfor j := range %s[i] { %s[%s+j] = %s(%s[i][j]) }\n",
					indent, expression, backing, offset, elementType, expression)
				fmt.Fprintf(out, "%s\t%s[i] = %s[%s:%s+len(%s[i])]\n",
					indent, name, backing, offset, offset, expression)
				fmt.Fprintf(out, "%s\t%s += len(%s[i])\n%s}\n", indent, offset, expression, indent)
			} else if f.Semantic == "binary-fixed" {
				fmt.Fprintf(out, "%sfor i := range %s { %s[i] = %s[i][:] }\n", indent, expression, name, expression)
			} else if f.ElementWise {
				fmt.Fprintf(out, "%sfor i := range %s {\n", indent, expression)
				fmt.Fprintf(out, "%s\tfor j := range %s[i] { %s[i][j] = byte(%s[i][j]) }\n",
					indent, expression, name, expression)
				fmt.Fprintf(out, "%s}\n", indent)
			} else {
				fmt.Fprintf(out, "%sfor i := range %s { %s[i] = %s(%s[i]) }\n", indent, expression, name, f.BaseType, expression)
			}
			valueExpr = name
		}
	} else if f.Scratch != "" {
		name := "w." + f.Scratch
		baseType := strings.TrimPrefix(f.BaseType, "[]")
		renderScratchResize(out, name, f.BaseType, expression, indent)
		fmt.Fprintf(out, "%sfor i := range %s { %s[i] = %s(%s[i]) }\n",
			indent, expression, name, baseType, expression)
		valueExpr = name
	} else if f.Semantic == "binary-fixed" {
		valueExpr = expression + "[:]"
	} else if f.ElementWise {
		*temp++
		name := fmt.Sprintf("eventheaderGenFixed%d", *temp)
		fmt.Fprintf(out, "%svar %s %s\n", indent, name, f.BaseType)
		fmt.Fprintf(out, "%sfor i := range %s { %s[i] = byte(%s[i]) }\n",
			indent, expression, name, expression)
		valueExpr = name
	} else if f.BaseType != "" && f.Semantic != "binary" && f.Semantic != "utf16" {
		valueExpr = fmt.Sprintf("%s(%s)", f.BaseType, expression)
	}
	fmt.Fprintf(out, "%sif err := w.binding.%s(%s); err != nil { return eventheader_gen_fmt.Errorf(%q, err) }\n",
		indent, f.Method, valueExpr, f.Path+": %w")
}

func renderScratchResize(out *strings.Builder, name, scratchType, expression, indent string) {
	fmt.Fprintf(out, "%sif cap(%s) < len(%s) {\n", indent, name, expression)
	fmt.Fprintf(out, "%s\tclear(%s)\n", indent, name)
	fmt.Fprintf(out, "%s\t%s = make(%s, len(%s))\n", indent, name, scratchType, expression)
	fmt.Fprintf(out, "%s} else {\n", indent)
	fmt.Fprintf(out, "%s\tif len(%s) > len(%s) { clear(%s[len(%s):]) }\n",
		indent, name, expression, name, expression)
	fmt.Fprintf(out, "%s\t%s = %s[:len(%s)]\n", indent, name, name, expression)
	fmt.Fprintf(out, "%s}\n", indent)
}

func renderScratchValidation(out *strings.Builder, f field, expression, indent string, temp *int) {
	const maxEventBytes = 65467
	countError := fmt.Sprintf("%s: %%w", f.Path)
	sizeError := countError

	if f.Array == "" {
		fmt.Fprintf(out, "%sif len(%s) > 65535 { return eventheader_gen_fmt.Errorf(%q, eventheader_gen_eventheader.ErrCountTooLarge) }\n",
			indent, expression, countError)
		width := 1
		if f.BaseType == "[]uint16" {
			width = 2
		}
		fmt.Fprintf(out, "%sif len(%s) > (%d-2)/%d { return eventheader_gen_fmt.Errorf(%q, eventheader_gen_eventheader.ErrEventTooLarge) }\n",
			indent, expression, maxEventBytes, width, sizeError)
		return
	}

	fmt.Fprintf(out, "%sif len(%s) > 65535 { return eventheader_gen_fmt.Errorf(%q, eventheader_gen_eventheader.ErrCountTooLarge) }\n",
		indent, expression, countError)
	prefix := 0
	if f.Array == "variable" {
		prefix = 2
	}
	if f.BaseType == "[]byte" || f.BaseType == "[]uint16" || f.BaseType == "string" {
		width := 1
		if f.BaseType == "[]uint16" {
			width = 2
		}
		*temp++
		size := fmt.Sprintf("eventheaderGenSize%d", *temp)
		fmt.Fprintf(out, "%s%s := %d\n", indent, size, prefix)
		fmt.Fprintf(out, "%sfor i := range %s {\n", indent, expression)
		fmt.Fprintf(out, "%s\tif len(%s[i]) > 65535 { return eventheader_gen_fmt.Errorf(%q, eventheader_gen_eventheader.ErrCountTooLarge) }\n",
			indent, expression, countError)
		fmt.Fprintf(out, "%s\tif %s > %d-2 || len(%s[i]) > (%d-%s-2)/%d { return eventheader_gen_fmt.Errorf(%q, eventheader_gen_eventheader.ErrEventTooLarge) }\n",
			indent, size, maxEventBytes, expression, maxEventBytes, size, width, sizeError)
		fmt.Fprintf(out, "%s\t%s += 2 + len(%s[i]) * %d\n", indent, size, expression, width)
		fmt.Fprintf(out, "%s}\n", indent)
		return
	}

	width := scratchElementWireSize(f)
	fmt.Fprintf(out, "%sif len(%s) > (%d-%d)/(%s) { return eventheader_gen_fmt.Errorf(%q, eventheader_gen_eventheader.ErrEventTooLarge) }\n",
		indent, expression, maxEventBytes, prefix, width, sizeError)
}

func scratchElementWireSize(f field) string {
	switch f.BaseType {
	case "int8", "uint8", "byte", "bool":
		return "1"
	case "int16", "uint16":
		return "2"
	case "int32", "uint32", "rune", "float32", "[4]byte":
		return "4"
	case "int64", "uint64", "float64":
		return "8"
	case "uintptr":
		return "int(^uintptr(0)>>63)*4 + 4"
	case "[16]byte":
		return "16"
	default:
		panic("eventheader-gen: unknown scratch element type " + f.BaseType)
	}
}
